    public SWTPc_CT_64() {
        screen = new byte[SCRROWS * SCRCOLS];
        scrolltmp = new byte[SCRCOLS];
        cursorpos = 0;
        clearscreen();
        UClock = true;
        initColors();
        charimgs = new Image[256];
        calculateCharImages();
    }

    private void initColors() {
        setBackground(Color.black);
        setForeground(Color.green);
    }

    private final Image charimgs[];

    private void calculateCharImages() {
        for (int i = 0; i < 256; i++) {
            charimgs[i] = new BufferedImage(CHARDOTCOLS, CHARDOTROWS,
                    BufferedImage.TYPE_BYTE_INDEXED);
            Graphics g = charimgs[i].getGraphics();
            int csi = (i & 0x7f) * CHARDOTROWS;
            if (i < 128) {
                g.setColor(BLANK_COLOR);
                g.fillRect(0, 0, CHARDOTCOLS, CHARDOTROWS);
                g.setColor(PHOSPHOR_COLOR);
            } else {
                g.setColor(PHOSPHOR_COLOR);
                g.fillRect(0, 0, CHARDOTCOLS, CHARDOTROWS);
                g.setColor(BLANK_COLOR);
            }
            int y = 0;
            for (int j = 0; j < CHARDOTROWS; j++) {
                int b = CHARSET_6575[csi];
                int x = 0;
                while (b != 0) {
                    if ((b & 0x80) != 0) {
                        g.fillRect(x, y, 1, 1);
                    }
                    b <<= 1;
                    x++;
                }
                csi++;
                y++;
            }
        }
        if (theFrame != null) {
            theFrame.setVisible(false);
            theFrame.invalidate();
            theFrame.pack();
            if (theAC30Frame != null) {
                theAC30Frame.setVisible(false);
                theAC30Frame.pack();
                theAC30Frame.invalidate();
                adjustCassetteBounds();
            }
            if (theGraphics != null) {
                showGraphics();
            }
            theFrame.setVisible(true);
            if (theAC30Frame != null) {
                theAC30Frame.setVisible(true);
            }
            requestFocus();
        }
    }

    public void powerOn() {
        theFrame = new Frame("SWTPc CT-64 Emulator");
        theFrame.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                System.exit(0);
            }
        });
        theFrame.setLayout(new BorderLayout());
        theFrame.add(this, "Center");
        Panel p = new Panel();
        Font f = new Font(Font.DIALOG, Font.PLAIN, Math.round(10 * DPI_SCALE));
        p.setFont(f);
        p.setFocusable(false);
        if (theComputer != null) {
            Button b = new Button("Reset MP68");
            b.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    theComputer.tripManualReset();
                }
            });
            b.setFocusable(false);
            p.add(b);
        }
        if (theProcessor != null) {
            Button b = new Button("Opcode Stats");
            b.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    theProcessor.printStats();
                }
            });
            b.setFocusable(false);
            p.add(b);
        }
        Checkbox cb = new Checkbox("Show Ctrl", ShowCtrl);
        cb.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                ShowCtrl = (e.getStateChange() == ItemEvent.SELECTED);
            }
        });
        cb.setFocusable(false);
        p.add(cb);
        cb = new Checkbox("U/C lock", UClock);
        cb.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                UClock = (e.getStateChange() == ItemEvent.SELECTED);
            }
        });
        cb.setFocusable(false);
        p.add(cb);
        theFrame.add(p, "South");
        theFrame.pack();
        theFrame.setVisible(true);
        if (theCassette != null) {
            showCassette();
        }
        if (theGraphics != null) {
            showGraphics();
        }
        requestFocus();
        addKeyListener(new KeyAdapter() {
            @Override
            public void keyTyped(KeyEvent e) {
                if (serialOut != null) {
                    char ch = e.getKeyChar();
                    if (ch == 0x0a && !e.isControlDown()) {
                        ch = 0x0d;
                    }
                    if (UClock) {
                        ch = Character.toUpperCase(ch);
                    }
                    //serialOut.waitForCTS();
                    serialOut.sendTo((byte) ch);
                }
            }
        });
    }

